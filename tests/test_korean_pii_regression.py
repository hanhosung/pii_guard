@dataclass
class FormatMetrics:
    """Recall metrics for one format variant (source_tag)."""
    tag: str
    total: int
    detected: int
    categories: FrozenSet[str]

    @property
    def recall(self) -> float:
        return self.detected / self.total if self.total else 0.0

    def __str__(self) -> str:
        cats = ",".join(sorted(self.categories))
        return (
            f"{self.tag:<40} [{cats}]  "
            f"{self.detected}/{self.total} = {self.recall:.0%}"
        )


@dataclass
class CategoryMetrics:
    """Aggregate precision/recall for one category."""
    category: str
    precision: float
    recall: float
    n_positive: int
    n_negative: int


@dataclass
class FalsePositiveRecord:
    """A single false positive detection on a negative sample."""
    source_tag: str
    text: str
    detected_categories: List[str]


@dataclass
class RegressionReport:
    """Full regression run results — built once per session."""
    format_metrics: Dict[str, FormatMetrics] = field(default_factory=dict)
    category_metrics: Dict[str, CategoryMetrics] = field(default_factory=dict)
    false_positives: List[FalsePositiveRecord] = field(default_factory=list)
    fp_by_neg_group: Dict[str, int] = field(default_factory=lambda: defaultdict(int))
    known_gap_tags: Set[str] = field(default_factory=set)

    def summary_table(self) -> str:
        """Human-readable metrics table for -s / verbose output."""
        lines = [
            "",
            "┌─────────────────────────────────────────────────────────────────────┐",
            "│       Korean PII Detection — Golden Regression Report               │",
            "├───────────────┬──────────────────┬──────────────────────────────────┤",
            "│ Category      │ Recall (floor)   │ Precision (floor)                │",
            "├───────────────┼──────────────────┼──────────────────────────────────┤",
        ]
        for cat, cm in sorted(self.category_metrics.items()):
            r_floor, p_floor = GOLDEN_AGGREGATE.get(cat, (0.0, 0.0))
            r_ok = "✓" if cm.recall >= r_floor else "✗"
            p_ok = "✓" if cm.precision >= p_floor else "✗"
            lines.append(
                f"│ {cat:<13} │ {cm.recall:.3f} {r_ok} (≥{r_floor:.2f})  "
                f"│ {cm.precision:.3f} {p_ok} (≥{p_floor:.2f})              │"
            )
        lines.append(
            "├───────────────┴──────────────────┴──────────────────────────────────┤"
        )
        lines.append("│ Per-format recall (format_tag : detected/total = recall)            │")
        lines.append("├──────────────────────────────────────────────────────────────────────┤")
        for tag, fm in sorted(self.format_metrics.items()):
            floor = GOLDEN_FORMAT_RECALL.get(tag, 0.0)
            ok = "✓" if fm.recall >= floor else "✗"
            gap = " [KNOWN GAP]" if tag in self.known_gap_tags else ""
            lines.append(
                f"│  {ok} {fm.tag:<38} {fm.detected}/{fm.total} = {fm.recall:.0%}"
                f" (≥{floor:.0%}){gap:<13}│"
            )
        lines.append("├──────────────────────────────────────────────────────────────────────┤")
        total_fps = len(self.false_positives)
        lines.append(f"│ False positives on negatives: {total_fps} (cap: ≤{GOLDEN_TOTAL_FP_CAP})                    │")
        for fp in self.false_positives:
            truncated = fp.text[:40]
            cats = ",".join(fp.detected_categories)
            lines.append(f"│  [{fp.source_tag}] {truncated!r} => {cats}")
        lines.append("└──────────────────────────────────────────────────────────────────────┘")
        return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# Session-scoped regression runner
# ─────────────────────────────────────────────────────────────────────────────

def _build_regression_report(corpus: KoreanPIICorpus, engine: Engine) -> RegressionReport:
    """
    Run the engine over the full corpus once and build a RegressionReport.

    This is called at most once per pytest session via the session-scoped
    fixture below.
    """
    report = RegressionReport()

    # Identify known-gap tags
    report.known_gap_tags = {
        tag for tag, floor in GOLDEN_FORMAT_RECALL.items() if floor == 0.0
    }

    # ── Per-format-variant recall ─────────────────────────────────────────────
    tag_total: Dict[str, int] = defaultdict(int)
    tag_detected: Dict[str, int] = defaultdict(int)
    tag_cats: Dict[str, Set[str]] = defaultdict(set)

    for sample in corpus.positive_samples():
        tag = sample.source_tag
        result = engine.scan(sample.text)
        detected_cats = {d.category for d in result.detections}
        tag_total[tag] += 1
        tag_cats[tag].update(sample.categories)
        if any(cat in detected_cats for cat in sample.categories):
            tag_detected[tag] += 1

    for tag in tag_total:
        report.format_metrics[tag] = FormatMetrics(
            tag=tag,
            total=tag_total[tag],
            detected=tag_detected[tag],
            categories=frozenset(tag_cats[tag]),
        )

    # ── Aggregate precision / recall per category ─────────────────────────────
    def detect_fn(text: str) -> Set[str]:
        res = engine.scan(text)
        return {d.category for d in res.detections}

    all_cats = set(GOLDEN_AGGREGATE.keys())
    for cat in sorted(all_cats):
        p, r = compute_precision_recall(corpus, detect_fn, cat)
        report.category_metrics[cat] = CategoryMetrics(
            category=cat,
            precision=p,
            recall=r,
            n_positive=len(corpus.samples_for_category(cat)),
            n_negative=len(corpus.negative_samples()),
        )

    # ── False positives on negative samples ───────────────────────────────────
    for sample in corpus.negative_samples():
        result = engine.scan(sample.text)
        if result.detections:
            cats = [d.category for d in result.detections]
            report.false_positives.append(FalsePositiveRecord(
                source_tag=sample.source_tag,
                text=sample.text,
                detected_categories=cats,
            ))
            report.fp_by_neg_group[sample.source_tag] += 1

    return report


@pytest.fixture(scope="session")
def korean_corpus() -> KoreanPIICorpus:
    """Deterministic Korean PII corpus (seed=42, 5 samples/format)."""
    return KoreanPIICorpus(seed=_CORPUS_SEED, samples_per_format=_SAMPLES_PER_FORMAT)


@pytest.fixture(scope="session")
def stage1_engine() -> Engine:
    """Stage-1 only Engine (no Stage-2 NER subprocess)."""
    return Engine()


@pytest.fixture(scope="session")
def regression_report(korean_corpus: KoreanPIICorpus, stage1_engine: Engine) -> RegressionReport:
    """
    Full regression report — computed once for the session.

    All test classes consume this fixture so the corpus is only scanned once.
    """
    return _build_regression_report(korean_corpus, stage1_engine)


