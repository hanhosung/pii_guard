"""
Golden regression test suite for Korean PII detection.
=======================================================

Tests the detection performance of the Stage-1 (regex + checksum) engine
against the deterministic synthetic Korean PII corpus.  Any rule change that
drops precision or recall below the floors pinned here will cause CI to fail,
preventing silent detection regressions.

Run (single command, produces pass/fail):
    pytest tests/test_korean_pii_regression.py -v

Verbose summary (prints full metrics table even on pass):
    pytest tests/test_korean_pii_regression.py -v -s

Coverage
--------
* 5 Korean PII categories:  PHONE, RRN, KR_ACCOUNT, PERSON, ADDRESS
* 37 positive format variants (per source_tag in KoreanPIICorpus seed=42)
* 36 negative samples used as the false-positive gate
* Per-category aggregate precision / recall floors
* Per-format-variant recall floor (catches targeted regressions)
* Known gaps documented explicitly (no silent pass-through)

How to update golden thresholds
---------------------------------
When a rule improvement intentionally raises precision or recall:

  1. Run: pytest tests/test_korean_pii_regression.py -v -s
     and confirm the new metrics in the summary table.
  2. Update the threshold constants in this file.
  3. Sign the rule-set change via update-manifest (see updater.py).
  4. Open a PR with the detection-rule changelog entry.

This file is part of the control plane — it must never be modified by an
automated agent.  Pin-list and threshold edits require out-of-band user review.

Baseline measurements (corpus seed=42, samples_per_format=5, Stage-1 only)
---------------------------------------------------------------------------
  PHONE       recall=1.000  precision=1.000
  RRN         recall=1.000  precision=1.000
  KR_ACCOUNT  recall=1.000  precision=0.984
  PERSON      recall=0.556  precision=1.000   [known gaps: sentence, en-label]
  ADDRESS     recall=0.840  precision=1.000

Known detection gaps (Stage-1 regex limitations — require Stage-2 NER):
  * PERSON — English-label format ("Name: 김철수"):
        Stage-1 person_labeled_en only matches Western capitalised names;
        Korean-script names after an English label are a Stage-2 gap.
  * PERSON — Sentence context ("고객 김철수님께서 요청하셨습니다."):
        Stage-1 has no unlabelled Korean person-name NER; Stage-2 required.
  * ADDRESS — Labelled format ("주소: <addr>"):
        The address_kr regex sometimes requires province-suffix words (도/시)
        to appear before the street token; bare province names miss some forms.
"""

