class TestAggregateThresholds:
    """
    Category-level precision / recall must stay above the golden floors.

    A failing test here means an across-the-board regression in at least one
    category.  Check per-format tests for the specific variant that regressed.
    """

    @pytest.mark.parametrize("category,floors", sorted(GOLDEN_AGGREGATE.items()))
    def test_recall_floor(
        self,
        regression_report: RegressionReport,
        category: str,
        floors: Tuple[float, float],
    ) -> None:
        min_recall, _ = floors
        cm = regression_report.category_metrics[category]
        assert cm.recall >= min_recall, (
            f"{category} recall REGRESSION: "
            f"got {cm.recall:.3f}, floor is {min_recall:.3f}  "
            f"({cm.n_positive} positive samples)"
        )

    @pytest.mark.parametrize("category,floors", sorted(GOLDEN_AGGREGATE.items()))
    def test_precision_floor(
        self,
        regression_report: RegressionReport,
        category: str,
        floors: Tuple[float, float],
    ) -> None:
        _, min_precision = floors
        cm = regression_report.category_metrics[category]
        assert cm.precision >= min_precision, (
            f"{category} precision REGRESSION (over-detection): "
            f"got {cm.precision:.3f}, floor is {min_precision:.3f}  "
            f"({cm.n_negative} negative samples used)"
        )

    def test_all_categories_measured(self, regression_report: RegressionReport) -> None:
        """Every category defined in GOLDEN_AGGREGATE must have a measurement."""
        for cat in GOLDEN_AGGREGATE:
            assert cat in regression_report.category_metrics, (
                f"Category {cat!r} has a golden threshold but no measurement. "
                f"Ensure the corpus contains samples for it."
            )


class TestFormatVariantRecall:
    """
    Per-format-variant recall floors.

    Each parametrised test covers one source_tag format.  A failure here
    tells you exactly which surface form of a category regressed, which
    narrows the root-cause to a specific regex change.

    Format variants with floor=0.0 are KNOWN GAPS (Stage-1 cannot detect them
    without NER).  They are listed here to document coverage, not to gate CI.
    Their tests always pass regardless of detection rate.
    """

    @pytest.mark.parametrize("tag,floor", sorted(GOLDEN_FORMAT_RECALL.items()))
    def test_format_recall(
        self,
        regression_report: RegressionReport,
        tag: str,
        floor: float,
    ) -> None:
        if tag not in regression_report.format_metrics:
            if floor == 0.0:
                pytest.skip(f"Format tag {tag!r} not present in corpus (known gap)")
            else:
                pytest.fail(
                    f"Format tag {tag!r} has floor {floor} but no corpus samples were found. "
                    f"The corpus generator may have changed. Check KoreanPIICorpus._build()."
                )

        fm = regression_report.format_metrics[tag]

        if floor == 0.0:
            # Known gap — document the actual rate but never fail
            return

        assert fm.recall >= floor, (
            f"Format variant REGRESSION: {tag!r}  "
            f"recall={fm.recall:.3f} < floor={floor:.3f}  "
            f"({fm.detected}/{fm.total} detected)  "
            f"Category: {','.join(sorted(fm.categories))}"
        )

    def test_all_format_tags_have_thresholds(
        self, regression_report: RegressionReport
    ) -> None:
        """
        Every format tag in the corpus must have an entry in GOLDEN_FORMAT_RECALL.

        Failure here means a new format variant was added to the corpus without
        a corresponding golden threshold — add one (or 0.0 for known gaps).
        """
        missing = set(regression_report.format_metrics.keys()) - set(GOLDEN_FORMAT_RECALL.keys())
        assert not missing, (
            f"New format tags found in corpus without golden thresholds: {sorted(missing)}\n"
            f"Add each to GOLDEN_FORMAT_RECALL in this file."
        )

    def test_all_threshold_tags_exist_in_corpus(
        self, regression_report: RegressionReport
    ) -> None:
        """
        Every tag in GOLDEN_FORMAT_RECALL (with floor > 0) must appear in the corpus.

        Failure means a format variant was removed from the corpus — either remove
        the threshold too, or restore the corpus generator to produce that variant.
        """
        gated_tags = {t for t, f in GOLDEN_FORMAT_RECALL.items() if f > 0.0}
        corpus_tags = set(regression_report.format_metrics.keys())
        orphaned = gated_tags - corpus_tags
        assert not orphaned, (
            f"Tags in GOLDEN_FORMAT_RECALL (floor>0) not found in corpus: {sorted(orphaned)}\n"
            f"Either the corpus no longer generates these variants, or the tag was renamed."
        )


class TestFalsePositiveGate:
    """
    Precision guard — must not exceed false-positive caps on negative samples.

    A failing test here means a rule is over-triggering.  Tighten the pattern
    or raise the min_confidence threshold for the offending rule.
    """

    def test_total_false_positives_within_cap(
        self, regression_report: RegressionReport
    ) -> None:
        total = len(regression_report.false_positives)
        assert total <= GOLDEN_TOTAL_FP_CAP, (
            f"Total false positives {total} exceeds cap {GOLDEN_TOTAL_FP_CAP}.  "
            f"Detections on negatives:\n"
            + "\n".join(
                f"  [{fp.source_tag}] {fp.text!r} => {fp.detected_categories}"
                for fp in regression_report.false_positives
            )
        )

    @pytest.mark.parametrize("neg_tag,cap", sorted(GOLDEN_FP_CAPS.items()))
    def test_fp_cap_by_negative_group(
        self,
        regression_report: RegressionReport,
        neg_tag: str,
        cap: int,
    ) -> None:
        actual = regression_report.fp_by_neg_group.get(neg_tag, 0)
        assert actual <= cap, (
            f"False-positive cap exceeded for negative group {neg_tag!r}: "
            f"{actual} > {cap}.  "
            f"FP samples:\n"
            + "\n".join(
                f"  {fp.text!r} => {fp.detected_categories}"
                for fp in regression_report.false_positives
                if fp.source_tag == neg_tag
            )
        )

    def test_no_false_person_detections(
        self, regression_report: RegressionReport
    ) -> None:
        """PERSON must never fire on any negative sample (high-precision check)."""
        person_fps = [
            fp for fp in regression_report.false_positives
            if "PERSON" in fp.detected_categories
        ]
        assert not person_fps, (
            f"PERSON fired on {len(person_fps)} negative sample(s):\n"
            + "\n".join(f"  [{fp.source_tag}] {fp.text!r}" for fp in person_fps)
        )

    def test_no_false_rrn_detections_on_invalid_rrns(
        self, regression_report: RegressionReport
    ) -> None:
        """
        RRN detector must not fire on the explicitly-invalid RRN negatives.

        The neg_rrn samples contain 13-digit strings that fail the checksum or
        have an invalid gender digit.  A FOREIGN_REG match on some of these is
        acceptable (different category); a RRN match is a checksum regression.
        """
        rrn_fps_on_neg_rrn = [
            fp for fp in regression_report.false_positives
            if fp.source_tag == "neg_rrn" and "RRN" in fp.detected_categories
        ]
        assert not rrn_fps_on_neg_rrn, (
            f"RRN detector fired on explicitly-invalid RRN negatives "
            f"({len(rrn_fps_on_neg_rrn)} samples). This is a checksum regression:\n"
            + "\n".join(f"  {fp.text!r}" for fp in rrn_fps_on_neg_rrn)
        )


class TestKnownGapsDocumented:
    """
    Verify that known Stage-1 coverage gaps exist and are properly documented.

    These tests PASS when the gap still exists (no false assurance).  If
    Stage-2 NER is later wired in, update GOLDEN_FORMAT_RECALL floors
    for the gap tags to a non-zero value.
    """

    def test_person_english_label_is_gap(
        self, regression_report: RegressionReport
    ) -> None:
        """
        Stage-1 cannot detect "Name: 김철수" because the labeled_en regex
        matches only Western capitalised names.  This is the documented gap.
        When Stage-2 NER is wired in for PERSON, update this test.
        """
        fm = regression_report.format_metrics.get("person_name_en_label")
        if fm is None:
            pytest.skip("No person_name_en_label samples in corpus")
        # We expect recall < 100% — if it becomes perfect, we should update thresholds
        # to a higher floor and remove this known-gap test.
        # For now, simply confirm the gap is documented (floor=0.0 in GOLDEN_FORMAT_RECALL).
        assert GOLDEN_FORMAT_RECALL.get("person_name_en_label", -1.0) == 0.0, (
            "person_name_en_label is unexpectedly gated at > 0.0. "
            "If Stage-1 now handles this format, raise the floor and remove this test."
        )

    def test_person_sentence_context_is_gap(
        self, regression_report: RegressionReport
    ) -> None:
        """
        Stage-1 cannot detect person names in unlabelled sentence context.
        This requires NER (Stage-2).  Documented as a known gap.
        """
        fm = regression_report.format_metrics.get("person_sentence")
        if fm is None:
            pytest.skip("No person_sentence samples in corpus")
        assert GOLDEN_FORMAT_RECALL.get("person_sentence", -1.0) == 0.0, (
            "person_sentence is unexpectedly gated. "
            "If NER is now active, update the floor and remove this test."
        )

    def test_known_gaps_never_silently_pass(
        self, regression_report: RegressionReport
    ) -> None:
        """
        Coverage gaps are explicitly declared — there must be NO format variant
        missing from GOLDEN_FORMAT_RECALL that also has zero recall.

        A variant with 0% recall and no floor entry would be an undocumented
        silent coverage gap — violating the NoSilentGap policy.
        """
        silent_gaps = []
        for tag, fm in regression_report.format_metrics.items():
            if fm.recall == 0.0 and tag not in GOLDEN_FORMAT_RECALL:
                silent_gaps.append(tag)
        assert not silent_gaps, (
            f"Undocumented zero-recall format variants detected — these are silent gaps:\n"
            + "\n".join(f"  {t}" for t in sorted(silent_gaps))
            + "\nAdd each to GOLDEN_FORMAT_RECALL with floor=0.0 and document the gap."
        )


class TestHighSeverityFloor:
    """
    High-severity categories (RRN, blocking categories) must maintain
    especially high recall floors even when other categories degrade.

    The GracefulDegradation policy says: "high-severity still caught" even
    when Stage-2 fails.  Stage-1 must be the backstop for these categories.
    """

    def test_rrn_recall_high_floor(self, regression_report: RegressionReport) -> None:
        """RRN recall must be ≥ 0.90 for Stage-1 to serve as fail-safe backstop."""
        cm = regression_report.category_metrics["RRN"]
        assert cm.recall >= 0.90, (
            f"RRN recall {cm.recall:.3f} < 0.90.  "
            f"RRN is a high-severity blocking category — Stage-1 must reliably catch it. "
            f"({cm.n_positive} positive samples)"
        )

    def test_rrn_precision_high_floor(self, regression_report: RegressionReport) -> None:
        """RRN precision must be ≥ 0.90 to avoid blocking on valid non-PII inputs."""
        cm = regression_report.category_metrics["RRN"]
        assert cm.precision >= 0.90, (
            f"RRN precision {cm.precision:.3f} < 0.90.  "
            f"Over-detection blocks legitimate requests. ({cm.n_negative} negative samples)"
        )

    @pytest.mark.parametrize("tag", [
        "rrn_dash", "rrn_bare", "rrn_space",
        "rrn_gender1", "rrn_gender2", "rrn_gender3",
    ])
    def test_rrn_format_variants_detected(
        self, regression_report: RegressionReport, tag: str
    ) -> None:
        """Every RRN surface form must be caught at high recall."""
        fm = regression_report.format_metrics.get(tag)
        assert fm is not None, f"RRN variant {tag!r} not found in corpus"
        assert fm.recall >= 0.80, (
            f"RRN format variant {tag!r} recall dropped to {fm.recall:.3f} "
            f"({fm.detected}/{fm.total}).  Check _RRN_PATTERN regex."
        )


class TestRegressionSummary:
    """
    Summary / reporting — always runs and prints the full metrics table.

    These tests do not assert additional thresholds; they produce human-readable
    output when running with ``-s``.  The table is the canonical evidence that
    detection efficacy was verified before a rule-set update is merged.
    """

    def test_print_summary_report(
        self,
        regression_report: RegressionReport,
        capsys,
    ) -> None:
        """Print the full regression report.  Always passes; read the output."""
        print(regression_report.summary_table())
        # Capture and check the report was non-empty
        captured = capsys.readouterr()
        assert "Korean PII Detection" in captured.out, (
            "Summary table was not printed — check the RegressionReport.summary_table() method"
        )

    def test_corpus_sizes_stable(self, korean_corpus: KoreanPIICorpus) -> None:
        """
        Corpus total sample count must be stable across runs.

        If this fails, the KoreanPIICorpus generator changed structure (new
        format variants or removed ones).  Update GOLDEN_FORMAT_RECALL accordingly.
        """
        pos = len(korean_corpus.positive_samples())
        neg = len(korean_corpus.negative_samples())
        # seed=42, samples_per_format=5 → 195 pos + 36 neg = 231 total
        # Breakdown: PERSON 45 + PHONE 30 + RRN 35 + ADDRESS 25 + KR_ACCOUNT 60 = 195
        assert pos >= 190, f"Fewer positive samples than expected: {pos} < 190"
        assert neg >= 30, f"Fewer negative samples than expected: {neg} < 30"
        # Allow slight growth (new format variants) but not shrinkage
        assert pos <= 500, f"Suspiciously many positive samples: {pos}"

    def test_category_counts_stable(self, korean_corpus: KoreanPIICorpus) -> None:
        """
        Per-category counts must match the corpus generator's documented output.
        If this fails, format variants were added or removed.
        """
        counts = korean_corpus.category_counts()
        expected_minimums = {
            "PERSON":     45,   # 9 format groups × 5
            "PHONE":      30,   # 6 format groups × 5
            "RRN":        35,   # 7 format groups × 5
            "ADDRESS":    25,   # 5 format groups × 5
            "KR_ACCOUNT": 60,   # 4 banks × 3 label styles × 5
        }
        for cat, minimum in expected_minimums.items():
            actual = counts.get(cat, 0)
            assert actual >= minimum, (
                f"{cat} category count {actual} < minimum {minimum}.  "
                f"Corpus generator may have dropped a format variant."
            )

    def test_regression_report_is_complete(
        self, regression_report: RegressionReport
    ) -> None:
        """All expected format tags must have been measured."""
        expected_tags = {t for t, f in GOLDEN_FORMAT_RECALL.items() if f > 0.0}
        missing = expected_tags - set(regression_report.format_metrics.keys())
        assert not missing, (
            f"Expected corpus format tags not measured: {sorted(missing)}.  "
            f"Either the corpus or the GOLDEN_FORMAT_RECALL dict needs updating."
        )
